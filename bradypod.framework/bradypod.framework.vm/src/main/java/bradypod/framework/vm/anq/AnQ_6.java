public class AnQ_6 {
	
	public static void main(String[] args) {
		
		// int:32位 ;byte:8位;char:16位
		
		System.out.println((byte)-1);
		
		System.out.println((char)(byte)-1);
		
		System.out.println((int)(char)(byte)-1);
		
	}
	
}
